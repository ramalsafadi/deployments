// Mock historical price data for charts
export const generateMockHistoricalData = (coinId, days = 30) => {
  const data = [];
  const basePrice = mockCoins.find(coin => coin.id === coinId)?.current_price || 100;
  const now = new Date();
  
  for (let i = days; i >= 0; i--) {
    const date = new Date(now.getTime() - i * 24 * 60 * 60 * 1000);
    const randomVariation = (Math.random() - 0.5) * 0.1; // Â±5% variation
    const price = basePrice * (1 + randomVariation * (i / days));
    
    data.push({
      date: date.toISOString().split('T')[0],
      timestamp: date.getTime(),
      price: parseFloat(price.toFixed(6)),
      volume: Math.floor(Math.random() * 1000000000)
    });
  }
  
  return data;
};

